public class ForLoopStatement{
    public static void main(String[] args){
		int num = 50;
		
		//syntax for for loop (i) stands for initialization expression
		// i <= 10 the condition
		//i++ means increament i by 1 
       
		for(int i = 1; i <= 10; i++);
			System.out.printf("%d %d%n",i,num);
	}
	
}
